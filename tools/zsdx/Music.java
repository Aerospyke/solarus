package zsdx;

/**
 * This class provides constants to handle the musics in the map editor.
 */
public class Music {

    /**
     * Special Id indicating that there is no music.
     */
    public static final String noneId = "none";

    /**
     * Special Id indicating that the music is unchanged.
     */
    public static final String unchangedId = "same";

    /**
     * String indicating that there is no music.
     */
    public static final String noneName = "<No music>";

    /**
     * String indicating that the music is unchanged.
     */
    public static final String unchangedName = "<Same as before>";

}
